package com.fengxf.feng.activity.ebuy.bean;

public class ExperienceForGoodsListBean {
	private String id =null;
	private String content =null;
	private String grade =null;
	private String love =null;
	private String realizetime =null;
	private String picurl =null;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getLove() {
		return love;
	}
	public void setLove(String love) {
		this.love = love;
	}
	public String getRealizetime() {
		return realizetime;
	}
	public void setRealizetime(String realizetime) {
		this.realizetime = realizetime;
	}
	public String getPicurl() {
		return picurl;
	}
	public void setPicurl(String picurl) {
		this.picurl = picurl;
	}
	 
}
