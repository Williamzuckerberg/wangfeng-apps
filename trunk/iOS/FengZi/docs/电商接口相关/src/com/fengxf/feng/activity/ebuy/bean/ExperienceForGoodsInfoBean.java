package com.fengxf.feng.activity.ebuy.bean;

public class ExperienceForGoodsInfoBean {
	private String id=null;
	private String title=null;
	private String content=null;
	private String picurl=null;
	private String realizetime=null;
	private String writer=null;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getContent() {
		return content;
	}
	public void setContent(String content) {
		this.content = content;
	}
	public String getPicurl() {
		return picurl;
	}
	public void setPicurl(String picurl) {
		this.picurl = picurl;
	}
	public String getRealizetime() {
		return realizetime;
	}
	public void setRealizetime(String realizetime) {
		this.realizetime = realizetime;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	
}
