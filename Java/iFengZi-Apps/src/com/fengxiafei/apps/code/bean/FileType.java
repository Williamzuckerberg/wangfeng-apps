package com.fengxiafei.apps.code.bean;

/**
 * 文件类型对照
 * 
 * @author wangfeng
 * @version 3.0.1 2012/05/27
 */
public class FileType {
	private int type;
	private String ext;

	/**
	 * @return the type
	 */
	public int getType() {
		return type;
	}

	/**
	 * @param type
	 *            the type to set
	 */
	public void setType(int type) {
		this.type = type;
	}

	/**
	 * @return the ext
	 */
	public String getExt() {
		return ext;
	}

	/**
	 * @param ext
	 *            the ext to set
	 */
	public void setExt(String ext) {
		this.ext = ext;
	}

}
